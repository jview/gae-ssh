package org.esblink.module.basedata.domain;

public class OptDataXmlVO {
	private Long id;
	private Long dataId;
	private String optXml;
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public Long getDataId() {
		return dataId;
	}
	public void setDataId(Long dataId) {
		this.dataId = dataId;
	}
	public String getOptXml() {
		return optXml;
	}
	public void setOptXml(String optXml) {
		this.optXml = optXml;
	}
	
}
